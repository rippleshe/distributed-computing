"""
computable.py — 用户程序接口定义

┌─────────────────────────────────────────────────────────────────┐
│                        系统框架（我们实现）                        │
│  ┌──────────┐    ┌──────────┐    ┌──────────┐    ┌──────────┐  │
│  │ 任务接收  │ →  │ 任务分发  │ →  │ 结果收集  │ →  │ 结果汇聚  │  │
│  └──────────┘    └──────────┘    └──────────┘    └──────────┘  │
└─────────────────────────────────────────────────────────────────┘
                              ↑ 调用
┌─────────────────────────────────────────────────────────────────┐
│                      用户程序（用户实现）                          │
│  ┌──────────────────────────────────────────────────────────┐  │
│  │              class Xxx(Computable):                       │  │
│  │  ┌────────┐      ┌────────┐      ┌────────┐             │  │
│  │  │ split() │      │compute()│      │ reduce()│             │  │
│  │  └────────┘      └────────┘      └────────┘             │  │
│  └──────────────────────────────────────────────────────────┘  │
└─────────────────────────────────────────────────────────────────┘

使用方式：
    1. 继承 Computable 类
    2. 实现 split()、compute()、reduce() 三个方法
    3. 将 .py 文件上传到系统
    4. 系统自动检测 Computable 子类并执行

设计思想：
    本接口仿照 Hadoop MapReduce 的 InputFormat / Mapper / Reducer 设计。
    用户只需关注"如何拆分数据"和"如何计算"两个问题，
    框架自动处理任务分发、并行执行、结果收集和容错恢复。
"""

from abc import ABC, abstractmethod
from typing import List


class Computable(ABC):
    """
    用户程序必须继承的抽象基类。

    三个方法对应 MapReduce 的三个阶段：

    ┌─────────────┐     ┌─────────────┐     ┌─────────────┐
    │   split()   │ →   │  compute()  │ →   │   reduce()  │
    │   分块       │     │   计算       │     │   汇聚       │
    │  Master 端   │     │  Worker 端   │     │  Master 端   │
    └─────────────┘     └─────────────┘     └─────────────┘

    继承示例：
        class SumCalculator(Computable):
            def split(self, input_data):
                # 将 "1,100" 切分成 ["1,25", "26,50", "51,75", "76,100"]
                ...

            def compute(self, slice_data):
                # 计算单个分片的和，如 "1,25" → "325"
                ...

            def reduce(self, results):
                # 将所有分片的和相加，如 ["325","950","1575","2200"] → "5050"
                ...

    注意事项：
    - 所有方法的输入和输出都是字符串类型，便于 JSON 序列化传输
    - split() 和 reduce() 在 Master 端执行，compute() 在 Worker 端执行
    - compute() 必须是无副作用的纯函数，不依赖外部状态
    """

    @abstractmethod
    def split(self, input_data: str) -> List[str]:
        """
        分块：将输入数据切分成多个独立的数据块（分片）。

        执行位置：Master 端（提交任务时立即执行）
        触发时机：客户端提交任务后，Master 调用此方法

        设计原则：
        - 每个分片必须是独立可计算的，不能依赖其他分片的结果
        - 分片数量影响并行度，建议根据数据规模合理划分
        - 分片过少 → 并行度低，分片过多 → 调度开销大

        参数:
            input_data: 原始输入数据
                        如果用户提交了输入参数，则为参数字符串（如 "1,100"）
                        如果用户上传了数据文件，则为文件内容

        返回:
            分片列表，每个元素是一个分片的输入数据（字符串）

        示例:
            输入: "1,100"
            返回: ["1,25", "26,50", "51,75", "76,100"]
        """
        pass

    @abstractmethod
    def compute(self, slice_data: str) -> str:
        """
        计算：处理单个数据分片。

        执行位置：Worker 端（可能有多个 Worker 并行执行）
        触发时机：Worker 从 Master 领取到分片后执行

        设计原则：
        - 此方法会被多个 Worker 并行调用，必须是线程安全的
        - 不应依赖任何外部状态或共享资源
        - 计算结果必须是确定性的（相同输入 → 相同输出）

        参数:
            slice_data: 单个分片的输入数据（由 split() 生成）

        返回:
            该分片的计算结果（字符串）

        示例:
            输入: "1,25"
            返回: "325"  (1+2+3+...+25)
        """
        pass

    @abstractmethod
    def reduce(self, results: List[str]) -> str:
        """
        汇聚：将所有分片的计算结果合并为最终结果。

        执行位置：Master 端
        触发时机：当某个任务的所有分片均完成时自动触发

        设计原则：
        - results 列表中每个元素对应一个分片的 compute() 返回值
        - results 的顺序与 split() 返回的分片顺序一致
        - 此方法只执行一次，必须能正确合并所有分片结果

        参数:
            results: 所有分片的计算结果列表
                     例如 ["325", "950", "1575", "2200"]

        返回:
            最终结果（字符串）

        示例:
            输入: ["325", "950", "1575", "2200"]
            返回: "5050"
        """
        pass
